//Write a program in java to swap two numbers.  
class Swap{
	public static void main(String args[]){

	int a=10,b=20,t;
	System.out.println("The Number before swap: ");
	System.out.println(""+a);
	System.out.println(""+b);
	
	t=a;
	a=b;
	b=t;

	System.out.println("The Number After swap: ");
	System.out.println(""+a);
	System.out.println(""+b);
	}


} 